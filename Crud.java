    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
       int id = Integer.parseInt(idText.getText());
     
        try {
            Machine m = stub.findById(id);
            refText.setText(m.getRef());
            marqueText.setText(m.getMarque());
            prixText.setText(String.valueOf(m.getPrix()));
            dateText.setDate(m.getDateAchat());
             JOptionPane.showMessageDialog(this,"Recherche effectue avec sucee");
        } catch (Exception e) {
           
            JOptionPane.showMessageDialog(this,e.getMessage(),"Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_searchActionPerformed

