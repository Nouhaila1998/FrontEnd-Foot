    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
       int id = Integer.parseInt(idText.getText());
       String ref = refText.getText();
       String marque = marqueText.getText();
       double prix = Double.parseDouble(prixText.getText());
       Date dateAchat = dateText.getDate();
       
       Machine m = new Machine(id,ref,marque,prix,dateAchat);
       
        try {
            stub.update(m);
            JOptionPane.showMessageDialog(this,"Modefication effectue avec sucee");
        } catch (Exception e) {
           
            JOptionPane.showMessageDialog(this,e.getMessage(),"Warning",JOptionPane.WARNING_MESSAGE);
        }
       
    }//GEN-LAST:event_editActionPerformed
        
